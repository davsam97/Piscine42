#include <unistd.h>

int	main(void)
{
	int	i;
	char	lc;
	char	uc;

	i = 0;
	while (i < 26)
	{
		if (i % 2 == 0)
		{
			lc = 'a' + i;
			write(1, &lc, 1);
		}
		else
		{
			uc = 'A' + i;
			write(1, &uc, 1);
		}
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
